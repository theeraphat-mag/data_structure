void append_postfix(int add){             
    node *n = malloc(sizeof(node));     //‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏´‡∏ô‡∏î
    n -> next = NULL;                   //‡πÉ‡∏´‡πâ n ‡∏ä‡∏µ‡πâ‡πÑ‡∏õ‡∏ó‡∏µ‡πà NULL
    if(head_p == NULL) head_p = n;      //‡∏ñ‡πâ‡∏≤ head == NULL ‡πÉ‡∏´‡πâ head = n
    else tail_p -> next = n;
    tail_p = n;
    n -> value = add;
}
